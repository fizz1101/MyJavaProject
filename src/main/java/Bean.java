public class Bean {

    private String sql;
    private Number se;
    private String key;

    public String getSql() {
        return sql;
    }

    public void setSql(String sql) {
        this.sql = sql;
    }

    public Number getSe() {
        return se;
    }

    public void setSe(Number se) {
        this.se = se;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
